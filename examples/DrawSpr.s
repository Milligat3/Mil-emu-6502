VIDEO_BUF = $F0

LDA #$00
STA VIDEO_BUF
LDA #$02
STA VIDEO_BUF+1

MAIN:

LDX #$00
LDA #$02
STA $F1

JSR DRAW_SPRITE
INC $F0
LDA $F0
CMP #$19
BEQ ResF0

JMP MAIN

ResF0:
LDA #$00
STA $F0
JMP MAIN



DRAW_SPRITE:
St_Dr:
LDY #$00
LDA SPRITE_ENEMY, X
STA $A0
IN_LOOP:
ASL $A0
BCC skip
LDA #$6F
STA ($F0), Y
skip:
INY
CPY #$08
BNE IN_LOOP

INX
CLC
LDA $F0
ADC #$20
STA $F0
LDA $F1
ADC #$00
STA $F1
CPX #$08
BNE St_Dr
RTS



SPRITE_PLAYER:
  .byte $00,$00,$66,$66,$18,$3C,$3C,$24
SPRITE_ENEMY:
  .byte $18,$3C,$7E,$DB,$FF,$24,$5A,$81